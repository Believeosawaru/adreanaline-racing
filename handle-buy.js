function insufShow() {
document.querySelector('.insufficient-funds').classList.add('show-insufficient');
    setTimeout(() => {
        document.querySelector('.insufficient-funds').classList.remove('show-insufficient');
    }, 1200)
}

function shopVehiclesBtnDisable(num) {
    document.querySelector(`.shop-vehicles-${num}`).style.display = 'block'
    document.querySelector(`.shop-vehicles-btn-${num}`).style.pointerEvents = 'none'
    exitShopPopup()
}

function shopItemsBtnDisable(num) {
    document.querySelector(`.shop-parts-${num}`).style.display = 'block'
    document.querySelector(`.shop-parts-btn-${num}`).style.pointerEvents = 'none'
    exitShopPopup()
}

function shopNitroBtnDisable(num) {
    document.querySelector(`.shop-nitro-${num}`).style.display = 'block'
    document.querySelector(`.shop-nitro-btn-${num}`).style.pointerEvents = 'none'
    exitShopPopup()
}

